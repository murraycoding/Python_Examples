# example of append method

# appending to an existing list
picnic = []
cheese_and_crackers = ["Cheese","Crackers"]

for item in cheese_and_crackers:
    picnic.append(item)

print(picnic)

# appending things to an already existing list
extra_things = ["Wine","Grapes","Strawberries"]

for thing in extra_things:
    picnic.append(thing)

print(picnic)